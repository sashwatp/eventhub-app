	var userID *uuid.UUID
	if userIDStr != "" {
		id, err := uuid.Parse(userIDStr)
		if err != nil {
			c.JSON(http.StatusBadRequest, gin.H{"error": "Invalid user ID"})
			return
		}
		userID = &id
	}

	events, total, err := ec.eventService.GetEventsByDateRange(startDate, endDate, page, pageSize, userID)
